package Exercise1;

public class Geometry {
    public void shape(int a, int b, int c){
        System.out.println("Triangles dimensions are "+a+" "+b+" "+c);
    }
    public void shape(int a){
        System.out.println("Circles radius is"+a);
    }
    public void shape(int a, int b, int c, int d){
        System.out.println("Square dimensions are "+a+" "+b+" "+c+" "+d);
    }
    public void shape(int a, int b, int c, int d, int e){
        System.out.println("Pentagon dimensions are "+a+" "+b+" "+c+" "+d+" "+e);
    }
}
