package Exercise4;

public class Mathemathics {
    public void add(int a, int b, int c){
        int d=a+b+c;
        System.out.println(d);
    }
    public void multiply(int a, int b, int c){
        int d=a*b*c;
        System.out.println(d);
    }
    public void remainder(double a, double b){
        double d=a%b;
        System.out.println(d);
    }
}
